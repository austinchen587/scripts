from typing import List, Dict
from content_analyzer import analyze_item_type

def build_db_summary(db_items: List[Dict]) -> str:
    """构建DB信息摘要"""
    if not db_items:
        return "DB中无商品信息"
    
    summary = "DB参考项目（{}个）：\n".format(len(db_items))
    for i, item in enumerate(db_items, 1):
        summary += f"{i}. {item.get('商品名称', '未知')}"
        if item.get('规格型号'):
            summary += f" ({item['规格型号'][:50]})"
        if item.get('采购数量'):
            summary += f" × {item['采购数量']}{item.get('单位', '')}"
        summary += "\n"
    
    return summary

def build_technical_procurement_prompt(doc_type: str, attachment_text: str) -> str:
    """构建技术类采购的专用提示词 - 基于测试样本优化版"""
    
    # 分析附件内容，确定商品类型
    item_type_hint = analyze_item_type(attachment_text)
    
    # 根据检测到的类型选择不同的技术参数模板
    main_type = ""
    if "检测到的商品类型：" in item_type_hint:
        for line in item_type_hint.split('\n'):
            if "检测到的商品类型：" in line:
                main_type = line.replace("【检测到的商品类型：", "").replace("】", "")
                break
    
    # 根据不同类型构建不同的技术参数模板
    tech_params_template = _get_tech_params_template(main_type)
    
    base_prompt = f"""
【技术类采购输出规范 - 基于商品类型智能调整】

【关键优化点】
1. 参数相关性：根据商品类型只保留相关技术参数
2. 避免填充：非相关商品不要填充"无相关参数"
3. 数量准确：严格从附件中提取准确数量
4. 规格完整：从表格或描述中提取完整的规格型号

{item_type_hint}

【输出格式要求】
根据商品类型使用合适的JSON结构：

{tech_params_template}

【提取规则】
1. 数量必须准确：从表格或清单中提取准确数量，不要自行推测
2. 规格型号完整：如果有具体尺寸/型号则写完整，否则描述关键特征
3. 技术参数相关：只提取与商品类型相关的技术参数
4. 避免参数填充：非相关商品不要包含空调/功率等无关参数
5. 商务要求：从专门的商务要求章节提取
6. 预算金额：从项目总预算或单价中提取

【常见问题处理指南】
1. 会议桌椅类：
   - 技术参数：尺寸、材质、颜色、环保标准
   - 不要包含：能效等级、功率、制冷量等电子设备参数

2. 图书类：
   - 规格型号：使用ISBN号
   - 技术参数：不需要技术参数字段
   - 备注：包含出版社、定价信息

3. 医疗设备类：
   - 技术参数：医疗专用参数（频率响应、采样率、精度）
   - 需要：医疗器械认证信息

4. 工业设备类：
   - 技术参数：压力范围、流量、材质、精度
   - 商务要求：质保期、售后服务响应时间

5. 服务类项目：
   - 商品名称：具体服务内容
   - 技术参数：服务内容、人员要求、成果交付
   - 不需要：硬件设备参数
"""
    
    return base_prompt

